	public void obtenerDuenyo() {
		ResultSet resultado = null;
		resultado = op.consultar("SELECT * FROM CLIENTES WHERE DNI = \""
				+ datos[13].toString() + "\"");

		if (resultado != null) {

			int numeroColumna = 0;
			try {
				numeroColumna = resultado.getMetaData().getColumnCount();
			} catch (SQLException ex) {
				Logger.getLogger(BuscarAnimal.class.getName()).log(
						Level.SEVERE, null, ex);
			}

			Object[] objetos = new Object[numeroColumna];
			for (int i = 1; i <= numeroColumna; i++) {
				try {
					objetos[i - 1] = resultado.getObject(i);
				} catch (SQLException ex) {
					Logger.getLogger(BuscarAnimal.class.getName()).log(
							Level.SEVERE, null, ex);
				}
			}
			cliente = objetos;

		}
		try {

			resultado.close();
		} catch (SQLException ex) {
			Logger.getLogger(BuscarAnimal.class.getName()).log(Level.SEVERE,
					null, ex);
		}

	}

