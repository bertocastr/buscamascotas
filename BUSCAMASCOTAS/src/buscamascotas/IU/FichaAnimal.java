	private void actualizarButtonActionPerformed(java.awt.event.ActionEvent evt) {

		String nombre = nombreText.getText();
		String chip = microchipText.getText();
		String fechaChip = fechaMicroText.getText();
		String sexo = sexoText.getText();
		String especie = especieText.getText();
		String raza = razaText.getText();
		String pelo = peloText.getText();
		String capa = capaText.getText();
		String dieta = dietaText.getText();
		String habitat = habitatText.getText();
		String nacimiento = nacimientoText.getText();
		String esterilizado = esterilizadoText.getText();

		PopUpWindow hola = new PopUpWindow(this, rootPaneCheckingEnabled);
		String textoLabel;

		if (nombre.equals("")) {
			textoLabel = "¿Dese actualizar los datos en BUSCAMASCOTAS?";
		} else {
			textoLabel = "¿Desea actualizar a " + nombre + " en BUSCAMASCOTAS?";
		}

		hola.setLabelPopup(textoLabel);
		hola.setVisible(true);
		if (hola.getReturnStatus() == 1) {

			setSQL("UPDATE ANIMALES SET NOM_ANIMAL ='" + nombre
					+ "', MICROCHIP='" + chip + "',FECHA_CHIP='" + fechaChip
					+ "',SEXO='" + sexo + "'," + "ESPECIE='" + especie
					+ "', RAZA='" + raza + "',TIPO_PELO='" + pelo + "',CAPA='"
					+ capa + "'," + "DIETA='" + dieta + "', HABITAT = '"
					+ habitat + "', FECHA_NACIMIENTO = '" + nacimiento + "', "
					+ "ESTERILIZADO = '" + esterilizado
					+ "' WHERE ID_CLIENTE = \"" + datos[13].toString()
					+ "\" AND NOM_ANIMAL = \"" + datos[1].toString() + "\"");

			op.insertar(getSQL());

			datos[1] = nombre;
			nombreText.setText(nombre);
			microchipText.setText(chip);
			fechaMicroText.setText(fechaChip);
			sexoText.setText(sexo);
			especieText.setText(especie);
			razaText.setText(raza);
			peloText.setText(pelo);
			capaText.setText(capa);
			dietaText.setText(dieta);
			habitatText.setText(habitat);
			nacimientoText.setText(nacimiento);
			esterilizadoText.setText(esterilizado);
		}
	}

